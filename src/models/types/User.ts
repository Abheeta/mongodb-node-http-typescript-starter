import { ObjectId } from "mongodb";

export interface User {
  _id?: ObjectId; // optional during creation
  id: number;     // your own numeric ID
  name: string;
  username: string;
  email: string;
  address: {
    street: string;
    suite: string;
    city: string;
    zipcode: string;
    geo: {
      lat: string;
      lng: string;
    };
  };
  phone: string;
  website: string;
  company: {
    name: string;
    catchPhrase: string;
    bs: string;
  };
}
